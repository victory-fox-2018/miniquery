 const SweetSelector = {
     select : function(input) {
        if (input[0]==='#') {
            return document.getElementById(input.slice(1))
        } else if (input[0]==='.') {
            return document.getElementsByClassName(input.slice(1))[0]
        } else {
            return document.getElementsByTagName(input)[0]
        }
     }
 }

